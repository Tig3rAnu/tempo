// Generate a flat array of all hashtags
export function getAllHashtags() {
  const all = new Set();
  HASHTAG_DATA.platform.forEach(h => all.add(h));
  Object.values(HASHTAG_DATA.countries).forEach(tags => tags.forEach(h => all.add(h)));
  HASHTAG_DATA.courses.forEach(h => all.add(h));
  HASHTAG_DATA.exams.forEach(h => all.add(h));
  HASHTAG_DATA.general.forEach(h => all.add(h));
  HASHTAG_DATA.universities.forEach(h => all.add(h));
  return Array.from(all);
}

// Get hashtags for a specific country
export function getCountryHashtags(country) {
  return HASHTAG_DATA.countries[country] || [];
}

// Get relevant hashtags for a course/program
export function getCourseHashtags(course) {
  const lower = course.toLowerCase();
  return HASHTAG_DATA.courses.filter(h =>
    h.toLowerCase().includes(lower) ||
    lower.includes(h.replace('#', '').toLowerCase())
  );
}

